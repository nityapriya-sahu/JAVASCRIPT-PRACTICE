// Print the numbers from 1 to 5 to the console using loop

console.log("Using for loop");
for (let i = 1; i <= 5; i++) {
  console.log(i);
}

// OR--
console.log("Using while loop");
let j = 1;
while (j <= 5) {
  console.log(j);
  j++;
}

// OR--
console.log("Using do-while loop");
let k = 1;
do {
  console.log(k);
  k++;
} while (k <= 5);
